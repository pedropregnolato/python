'''Faça um programa que exiba a área de um triangulo.'''

b = int(input("Base: "))
alt = int(input("Altura: "))

area = (b * alt) / 2

print("A área do triangulo é: ", area)


'''Faça um programa que exiba a área de um quadrado.'''

l1 = int(input("Lado 1: "))
l2 = int(input("Lado 2: "))

area = l1 * l2

print("A área do quadrado é: ", area)




