''' Faça um programa que leia o nome, a idade, a altura, o peso e a nacionalidade do 
usuário e escreva essas informações na forma de um parágrafo de apresentação. '''

nome = str(input("Nome: "))
idade = int(input("Idade: "))
altura = float(input("Altura: "))
peso = float(input("Peso: "))
nacionalidade = str(input("Nacionalidade: "))

print(nome, idade, altura, peso, nacionalidade)
